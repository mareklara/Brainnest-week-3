
const radius = 4;
//Finds the circumference
function circumference (r){
    const equation = 2 * Math.PI * radius;
    return equation;
}
console.log(circumference(radius));
//Finds the area
function area(){
    const solution = radius * 4;
    const area1 = Math.PI * solution;
    return area1;
}
